void led_display(signed char m, signed char n){   // 設定亮燈
	const char mode[] = { 0x00, 0x01, 0x02, 0x03 , 0x04, 0x05, 0x06, 0x07 };   //亮的位置 //最左邊為0x00
	const char seg_code[] = {0x07, 0x7F, 0x6F, 0x77,	//789+	//A代表加
						     0x66, 0x6D, 0x7D, 0x40,	//456-	//-代表減
						     0x06, 0x5B, 0x4F, 0x76,	//123*	//H代表乘
							 0x58, 0x3F, 0x48, 0x5E,	//c0=/	//c代表clean =就是= d代表除
							 0x00, 0x79, 0x50, 0x5C,	//全暗 //E //r //o	
							 0x7C, 0x58, 0x5E, 0x71		//bcdF
							};    
	
	P1 = mode[m];
	P2 = seg_code[n];
}